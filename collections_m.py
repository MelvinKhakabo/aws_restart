#working with lists
myFruitList = ["apple", "banana", "cherry"]
print(myFruitList)
print(type(myFruitList))

#accessing list itmes by position
print(myFruitList[0])
print(myFruitList[1])
print(myFruitList[2])

#changing items in a list
myFruitList[2] = "orange"
print(myFruitList)



